#python program to perform basic arithmetic operation (+ , - , รท, *)

#initialize num1
num1 = input ("Enter value of num1 : ")

#initialize num2
num2 = input ("Enter value of num2 : ")

#formula of sum
sum = float(num1) + float(num2)

#formula of subtract
sub = float(num1) - float(num2)

#formula of division
div = float(num1) / float(num2)

#formula of multiplication
mul = float(num1) * float(num2)

#print output all arithmetic operation
print ("total sum is ", sum)
print ("total sub is ", sub)
print ("total division is ", div)
print ("total multiplication is ", mul)